#include <iostream>
using namespace std;

int main ()
{
    cout << "Char " << sizeof(char) << endl;
    cout << "Boolean " << sizeof(bool) << endl;
    cout << "Integer " << sizeof(int) << endl;
    cout << "Double " << sizeof(double) << endl;
    cout << "Unsigned " << sizeof(unsigned) << endl;
    return 0;
}